					<h2>The Centre for Urban Equity and Health (CUEH)</h2>
					<p>The Centre for Urban Equity and Health (CUEH) fosters innovation on how to redress urban health inequities within Bangladesh and globally. CUEH serves as a knowledge hub on urban health equity with a specific focus on the social determinants of inequities in health, and the health system issues of governance, information, human resources and service delivery. Through collaborative activities and joint learning, CUEH galvanizes evidence-informed discourse and decision-making, and supports partnerships and networking among government policy makers, private and public service providers, researchers and development partners.</p>
					<p>CUEH is well-positioned to analyze and address the health equity impacts of explosive urban population growth and nurtures global linkages with individuals and institutions through research, training and policy forums. These collaborations provide opportunities to identify common challenges and innovate cost-effective, intersectoral solutions that can be replicated and adapted to redress urban health inequities within and beyond Bangladesh.</p>
					<p>The ultimate goal of the CUEH’s urban health entrepreneur is to innovate sustainability by engaging community and investing in efforts that build resilience and equity despite the pressures of urban population growth.</p>
					<p>CUEH models good governance practices by embracing a broad participation of relevant sectors (health, housing, transport etc.) and stakeholders (development partners, local government, NGOs etc.), and applying evidence-based approaches to building consensus, aligning resources and coordinating action.</p>

