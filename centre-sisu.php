					<h2>The Centre of Excellence on the Science of Implementation and Scale-Up (CoE-SISU)</h2>
					<p>The Centre of Excellence on the Science of Implementation and Scale-Up (CoE-SISU) was set up in 2016 in partnership between the BRAC James P Grant School of Public Health, BRAC University and UNICEF Country Office, Bangladesh to generate evidence-based interventions and innovations for policy-makers and implementers and to adopt them in meeting the special challenges in accessing quality health care services for children and women in Bangladesh.</p>
