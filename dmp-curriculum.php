						<h3>Pop up Fairs</h3>
						<p>Pop up fairs are organized monthly to advocate the midwifery profession in the community and provide midwifery services to the women in the community. On the selected day students go to the community along with the teachers and select a place in the community. There are various stalls set up such as: ANC corner, PNC corner, FP corner, Breast examination corner.</p>
						<div style="display: inline;" id="dmp-open-school"></div>
						<p>In the community the Midwife provides ANTENATAL CARE, POSTNATAL CARE, NEWBORN CARE, FAMILY PLANNING SERVICES, examines the breast of the women and if any abnormality is found they refer them to a doctor. They also provide adolescence health education, hygiene and nutrition education, hand washing technique to the community. Course coordinators prepare a report of the activities and send it to the HUB.</p>
					</div>
					<div class="mph-content-block">
						<h3>Open School Day</h3>
						<p>Open school day is celebrated every year in all academic sites and is generally arranged to advocate midwifery education through providing midwifery services to the community women. Leading people of the community and a special guest from government officials are invited to the program. On this day the students prepare stalls (ANC corner, PNC corner, FP corner, Breast examination corner) and invite people from the community to provide midwifery services. The Midwives provide Antenatal care, postnatal care, newborn care, family planning services, examine the breast of the women and if any abnormality is found they refer them to a doctor. They also provide adolescence health education, hygiene and nutrition education, hand washing technique to the community. In the evening a cultural program is held. The student presents jarigan, dance and educational drama to grow awareness in the community.</p>
						<div style="display: inline;" id="dmp-faculty"></div>
