					<div class="mph-content-block" id="dmp-course-des">
						<h3>Course Description </h3>
						<p>Developing Midwives Project is a 3 year long diploma course designed with a 2 pronged approach, i.e :</p>
						<ul>
							<li>Developing faculty for midwifery education</li>
							<li>Producing Qualified midwives</li>
						</ul>
						<p>The course is a 174 credits residential programme, consisting of 40 percent theoretical and 60 percent practical. It is divided into 6 semesters which is completed in 3 years, allowing each semester to have 2 years. During this time they continue to work in local hospitals, in clinics and/or health centers and in communities to practice their newly acquired knowledge and skills in the clinical setting. It emphasizes proficiency in English, community engagement. After completion of the 3 years course, there is a provision of a 6 month internship.</p>
						<div style="display: inline;" id="dmp-community"></div>
					</div>
					<div class="mph-content-block">
						<h3>Community Engagement</h3>
						<p>A uniqueness of the Diploma in Midwifery program is to keep the students and midwives engaged in the community through different forms of interaction throughout the entire course. During the community visit, they identify community health problems, provide antenatal care, post-natal services to mothers, prepare class assignments, projects, and case studies based on interactions with the community. It helps Midwifery students develop insights and gather experience from the community. There are various community-based activities like pop up fairs, counseling on nutrition, personal hygiene, adolescent health and different methods of family planning. These activities increase visibility of the midwives and students and enhance community trust in them which ultimately leads towards wider acceptance of midwifery led services.</p>
						<div style="display: inline;" id="dmp-internship"></div>
					</div>
					<div class="mph-content-block">
						<h3>Internship</h3>
						<p>For completion of the degree a student has to accomplish their internship which lasts for six months. All academic sites take responsibility to place the graduate midwives in the clinical sites where cases are available for enough practice.</p>
						<p>There is an internship guideline called CEGM (Community engagement of graduate midwives) for graduate midwives. Before starting the internship, an orientation session is organized through which they follow the instructions to achieve targets of NVD, ANC, PNC and newborn care per month. They have to serve the family planning commodities at the community or clinical site. A reporting day is compulsory to report their activities to the academic site every month, NVDs and family planning services (commodities) are verified by the faculty and Sr. Instructor. The Midwives, even with a job, should go through the internship process and achieve the target and report to the academic site. After successful completion of the internship, a certificate is provided by BRAC University. The internship as a process develops their skills in relation to midwifery services.</p>
