					<div class="mph-content-block">
						<h3>Faculty Development</h3>
						<div style="display: inline;" id="dmp-e-learning"></div>
						<p>Faculty Development has been an ongoing skill development process which takes place twice a year. Training refreshes and updates knowledge of faculties where renowned resource persons from home and abroad takes classes. Auckland University of Technology (AUT) provide technical support in curriculum development. <br><br>
						<a href="dmp-news-events.php#Faculty-development">Read about our latest faculty development program here</a></p>
					</div>
					<div class="mph-content-block" id="">
						<h3>E-Learning Methodology</h3>
						<p>DMP has introduced E-Learning methodology to the midwifery students and faculty where they gather updated knowledge on midwifery and collect relevant information. They are often encouraged to access the E-Learning platforms such as “Global Health E-Learning.” which offers different learning opportunities to internalize midwifery curriculum and modules. Modules related to antenatal care, emergency obstetric and newborn care, community health, patient safety, nutrition, complication during pregnancy etc. have been recommended for online based knowledge resources as an integral part of study. The faculty can refresh their knowledge and acquire certificates from the Global Health Networks. E-learning platforms like AUT Blackboard and Dropbox are used by the faculty and they’re encouraged to share the essential information with students.</p>
						<div style="display: inline;" id="dmp-mlc"></div>
					</div>
					<div class="mph-content-block" id="">
						<h3>Demonstration Centre: Midwife-Led Care Centre(MLC):</h3>
						<p>The Midwife-Led Centre, a 24/7 maternity centre led and operated by the midwives has been established in an effort to provide quality midwifery-led respectful maternity care to the community. The centres are located in Mirpur, Dhaka, Jaintiapur, Sylhet and Dumuria, Khulna. These MLCs are promoting the alternative birth positions, pain relief exercises to ease the normal vaginal delivery. These innovative endeavors have contributed to huge patient flow.</p>
						<p>This model centre enables the midwifery students to learn how to provide professional midwifery services and promote midwifery professions to the community. The services have also continued during COVID-19 pandemic since March 2020. The Midwives ensure personal protection, maintain proper social distancing, hygiene and screening of body temperature of the clients before offering services. As of August 2020 a total 2567 normal deliveries has been conducted by the midwives at the MLCs, of those, 71% clients accepted the alternative birthing chair deliveries; 5003 women participated in the exercise sessions; 7755 women received ante-natal checkup and 2296 women received postnatal checkup.</p>
