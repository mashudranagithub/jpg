					<h2>The Center of Excellence on Universal Health Coverage (CoE-HS&UHC)</h2>
					<p>The Center of Excellence on Universal Health Coverage was formed to accelerate evidence informed, equitable and sustainable action towards Universal Health Coverage (UHC) in Bangladesh and globally with support from the Rockefeller Foundation.</p>
					<p>The Centre serves both as a national and global catalyst for evidence informed policy and practice and the development of country-based cadres necessary to implement UHC. The Centre also works with a broad coalition of partners within Bangladesh and internationally on advancing practical efforts to accelerate UHC. The Centre contributes to three critical elements to the broader agenda that fits within its comparative advantages of being an academic institution nested in a world-renowned, entrepreneurial NGO, BRAC, and working in close partnership with a premiere international research centre, ICDDR,B. The elements are:</p>
					<ul>
						<li>Essential evidence necessary to guide the design of UHC policies and programs and to monitor and evaluate their implementation;</li>
						<li>Core competencies for effective policy and practice towards UHC acquired through on-the-job and pre-service training;</li>
						<li>A UHC Forum providing key stakeholders with a continuous opportunity for reporting and learning on best practices for UHC</li>
					</ul>
					<p>The Centre also acts as a co-secretariat for the Global Task Force on UHC ensuring strong Southern leadership and facilitating a synergistic interface between the work in Bangladesh and efforts towards UHC regionally and globally.</p>

